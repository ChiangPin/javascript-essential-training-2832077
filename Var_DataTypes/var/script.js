/**
 * The var statement (function/globally-scoped variable)
 * default for all variables
 * mutable, can change type, e.g. number to string
 * can define multiple diff variables at the same time, e.g. var x = 4, y = 5;
 * @link https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/var
 */
var x = 4, y = 5;
var empty;
console.log("x:", x);
console.log("y:", y);
console.log("empty:", empty);

